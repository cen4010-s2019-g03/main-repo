			if(target.length > 0) {
				event.preventDefault();
				if($(this.hash).offset()) {
					var offset = $(this.hash).offset().top;
				}
				else {
					var offset = 0;
				}
				$('html, body').animate({scrollTop: offset - 56}, 300);
			}
